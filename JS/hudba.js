let indexKpop = 0;
let indexMetal = 0;
let indexRock = 0;
let indexKlassic = 0;

vypis(indexKpop, kpop, sectionKpop);
vypis(indexMetal, metal, sectionMetal);
vypis(indexRock, rock, sectionRock);
vypis(indexKlassic, klassic, sectionKlassic);

tlPredchoziK.addEventListener('click', function(){
    if(indexKpop === 0){
      indexKpop = kpop.length - 1;
    }else{
      indexKpop--;
    }
    vypis(indexKpop, kpop, sectionKpop);
  });

  tlNasledujiciK.addEventListener('click', function(){
    if(indexKpop === kpop.length - 1){
      indexKpop = 0;
    }else{
      indexKpop++;
    }
    vypis(indexKpop, kpop, sectionKpop);
  });

